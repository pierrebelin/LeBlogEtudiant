<?php

namespace Pierre\SendinBlueBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * BlockedMail
 *
 * @ORM\Table(name="blocked_mail")
 * @ORM\Entity(repositoryClass="Pierre\SendinBlueBundle\Repository\BlockedMailRepository")
 */
class BlockedMail
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="mail", type="string", length=255, unique=true)
     */
    private $mail;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set mail
     *
     * @param string $mail
     *
     * @return BlockedMail
     */
    public function setMail($mail)
    {
        $this->mail = $mail;

        return $this;
    }

    /**
     * Get mail
     *
     * @return string
     */
    public function getMail()
    {
        return $this->mail;
    }
}

